set search_path = ''
as $$
  select exists (
    select 1 from public.leagues
    where id = target_league_id and owner_id = (select auth.uid())
  ) or exists (
    select 1 from public.league_members
    where league_id = target_league_id
      and user_id = (select auth.uid())
      and status = 'accepted'
      and role in ('owner', 'admin')
  );
$$;

