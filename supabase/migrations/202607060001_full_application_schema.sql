-- Enable RLS everywhere exposed through the public schema.
do $$
declare
  table_name text;
begin
  foreach table_name in array array[
    'users', 'exercise_metrics', 'exercises', 'exercise_muscle_groups',
    'foods', 'body_metrics', 'muscle_groups', 'nutrients', 'user_days',
    'day_muscle_targets', 'meals', 'meal_items', 'meal_nutrients',
    'workout_sessions', 'workout_exercises', 'exercise_sets',
    'body_measurements', 'friendships', 'leagues', 'league_members',
    'league_metrics', 'league_metric_results', 'league_results'
  ] loop
    execute format('alter table public.%I enable row level security', table_name);
    execute format('revoke all on table public.%I from anon', table_name);
    execute format('grant all on table public.%I to service_role', table_name);
  end loop;
end;
$$;

do $$
declare
  table_name text;
begin
  foreach table_name in array array[
    'exercise_metrics', 'exercises', 'exercise_muscle_groups', 'foods',
    'body_metrics', 'muscle_groups', 'nutrients'
  ] loop
    execute format('grant select on table public.%I to authenticated', table_name);
    execute format('drop policy if exists reference_read_authenticated on public.%I', table_name);
    execute format(
      'create policy reference_read_authenticated on public.%I for select to authenticated using (true)',
      table_name
    );
  end loop;
end;
$$;

grant select, update on table public.users to authenticated;
drop policy if exists users_select_own_profile on public.users;
create policy users_select_own_profile
  on public.users for select to authenticated
  using ((select auth.uid()) = id);
drop policy if exists users_update_own_profile on public.users;
create policy users_update_own_profile
  on public.users for update to authenticated
  using ((select auth.uid()) = id)
  with check ((select auth.uid()) = id);

do $$
declare
  table_name text;
begin
  foreach table_name in array array[
    'user_days', 'day_muscle_targets', 'meals', 'meal_items',
    'meal_nutrients', 'workout_sessions', 'workout_exercises',
    'exercise_sets', 'body_measurements'
  ] loop
    execute format('grant select, insert, update, delete on table public.%I to authenticated', table_name);
    execute format('drop policy if exists owner_all on public.%I', table_name);
    execute format(
      'create policy owner_all on public.%I for all to authenticated '
      'using ((select auth.uid()) = user_id) '
      'with check ((select auth.uid()) = user_id)',
      table_name
    );
  end loop;
end;
$$;

grant select, insert on table public.friendships to authenticated;
revoke update, delete on table public.friendships from authenticated;
drop policy if exists friendships_participant_read on public.friendships;
create policy friendships_participant_read
  on public.friendships for select to authenticated
  using ((select auth.uid()) in (user_a_id, user_b_id));
drop policy if exists friendships_request on public.friendships;
create policy friendships_request
  on public.friendships for insert to authenticated
  with check (
    requested_by = (select auth.uid())
    and (select auth.uid()) in (user_a_id, user_b_id)
    and status = 'pending'
  );
drop policy if exists friendships_participant_update on public.friendships;

grant select, insert, update, delete on table public.leagues to authenticated;
drop policy if exists leagues_member_read on public.leagues;
create policy leagues_member_read
  on public.leagues for select to authenticated
  using (owner_id = (select auth.uid()) or public.is_league_member(id));
drop policy if exists leagues_owner_insert on public.leagues;
create policy leagues_owner_insert
  on public.leagues for insert to authenticated
  with check (owner_id = (select auth.uid()));
drop policy if exists leagues_owner_update on public.leagues;
create policy leagues_owner_update
  on public.leagues for update to authenticated
  using (owner_id = (select auth.uid()))
  with check (owner_id = (select auth.uid()));
drop policy if exists leagues_owner_delete on public.leagues;
create policy leagues_owner_delete
  on public.leagues for delete to authenticated
  using (owner_id = (select auth.uid()));

grant select, insert, update, delete on table public.league_members to authenticated;
