create or replace view public.nutrient_intake_facts
with (security_invoker = true)
as
select
  ud.user_id,
  ud.log_date,
  extract(isodow from ud.log_date)::smallint as weekday_iso,
  m.id as meal_id,
  m.kind as meal_kind,
  m.meal_number,
  mi.id as meal_item_id,
  mi.food_code,
  f.name as food_name,
  n.id as nutrient_id,
  n.key as nutrient_key,
  n.name as nutrient_name,
  mn.amount,
  n.canonical_unit,
  mn.source
from public.meal_nutrients mn
join public.meal_items mi on mi.id = mn.meal_item_id and mi.user_id = mn.user_id
join public.meals m on m.id = mi.meal_id and m.user_id = mi.user_id
join public.user_days ud on ud.id = m.day_id and ud.user_id = m.user_id
join public.foods f on f.code = mi.food_code
join public.nutrients n on n.id = mn.nutrient_id;

