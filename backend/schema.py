USERS_SQL = """
create table if not exists public.users (
  id uuid primary key references auth.users(id) on delete cascade,
  username text not null unique,
  display_name text,
  profile_picture text,
  pace_gender text,
  goals text[] not null default '{}',
  workout_experience text,
  cuisine_preferences text[] not null default '{}',
  dietary_preferences text[] not null default '{}',
  preferred_units text not null default 'metric',
  bio text,
  onboarding_completed boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint users_username_format check (username ~ '^[a-z0-9_]{3,30}$'),
  constraint users_profile_picture_length check (profile_picture is null or length(profile_picture) <= 500),
  constraint users_pace_gender_check check (pace_gender is null or pace_gender in ('female', 'male')),
  constraint users_workout_experience_check check (workout_experience is null or workout_experience in ('beginner', 'intermediate', 'advanced')),
  constraint users_preferred_units_check check (preferred_units in ('metric', 'imperial')),
  constraint users_bio_length check (bio is null or length(bio) <= 500)
);

alter table public.users add column if not exists display_name text;
alter table public.users add column if not exists profile_picture text check (profile_picture is null or length(profile_picture) <= 500);
alter table public.users add column if not exists pace_gender text check (pace_gender is null or pace_gender in ('female', 'male'));
alter table public.users add column if not exists goals text[] not null default '{}';
alter table public.users add column if not exists workout_experience text check (workout_experience is null or workout_experience in ('beginner', 'intermediate', 'advanced'));
alter table public.users add column if not exists cuisine_preferences text[] not null default '{}';
alter table public.users add column if not exists dietary_preferences text[] not null default '{}';
alter table public.users add column if not exists preferred_units text not null default 'metric' check (preferred_units in ('metric', 'imperial'));
alter table public.users add column if not exists bio text check (bio is null or length(bio) <= 500);
alter table public.users add column if not exists onboarding_completed boolean not null default false;

insert into storage.buckets (id, name, public, file_size_limit, allowed_mime_types)
values ('profile-pictures', 'profile-pictures', true, 1048576, array['image/png'])
on conflict (id) do update set
  public = excluded.public,
  file_size_limit = excluded.file_size_limit,
  allowed_mime_types = excluded.allowed_mime_types;

alter table public.users enable row level security;

revoke all on table public.users from anon;
grant select, update on table public.users to authenticated;
grant all on table public.users to service_role;

drop policy if exists "users_select_own_profile" on public.users;
create policy "users_select_own_profile"
  on public.users for select
  to authenticated
  using ((select auth.uid()) = id);

drop policy if exists "users_update_own_profile" on public.users;
create policy "users_update_own_profile"
  on public.users for update
  to authenticated
  using ((select auth.uid()) = id)
  with check ((select auth.uid()) = id);

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
declare
  requested_username text;
  requested_display_name text;
begin
  requested_username := lower(trim(new.raw_user_meta_data ->> 'username'));
  requested_display_name := nullif(trim(new.raw_user_meta_data ->> 'display_name'), '');
  if requested_username is null or requested_username !~ '^[a-z0-9_]{3,30}$' then
    raise exception 'invalid username';
  end if;

  insert into public.users (id, username, display_name)
  values (new.id, requested_username, requested_display_name)
  on conflict (id) do nothing;
  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();

notify pgrst, 'reload schema';
""".strip()

