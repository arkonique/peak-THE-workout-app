FOODS_SQL = """
create extension if not exists pg_trgm;

create table if not exists public.foods (
  code text primary key,
  name text not null
);

create index if not exists foods_name_trgm_gist_idx
  on public.foods using gist (name gist_trgm_ops);

alter table public.foods enable row level security;

create or replace function public.search_food_names(
  search_text text,
  result_limit integer default 5
)
returns table (code text, name text, score real)
language sql
stable
security invoker
set search_path = public
as $$
  select
    foods.code,
    foods.name,
    (1 - (foods.name <-> trim(search_text)))::real as score
  from public.foods
  where length(trim(search_text)) >= 2
  order by foods.name <-> trim(search_text)
  limit least(greatest(result_limit, 1), 5);
$$;

notify pgrst, 'reload schema';
""".strip()

